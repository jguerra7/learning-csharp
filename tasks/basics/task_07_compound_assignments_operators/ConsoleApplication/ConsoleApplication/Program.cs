using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApplication
{
    class Program
    {
        static void Main(string[] args)
        {
            // Compound Assignment Operators
            int value = 10;
            value += 10;        // value = 20
            value -= 5;         // value = 15
            value *= 10;        // value = 150
            value /= 3;         // value = 50
            value %= 8;         // value = 2
        }
    }
}
